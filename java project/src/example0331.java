
public class example0331 {
	
	public static void main(String[] args) {
		
		//변수 지정
		int hour = 3;
		int minute = 5;	
		System.out.println(hour+"시간"+minute + "분");
		
		//변수 총 시간
		int totalMinute = (hour*60) + minute;
		
		//결과값 출력
		System.out.println("총" +totalMinute+"분");
				
	}
}
